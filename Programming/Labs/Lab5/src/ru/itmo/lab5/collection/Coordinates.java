package ru.itmo.lab5.collection;

public class Coordinates 
{
    private double x;
    private long y;
    
    public void setX(double x)
    {
    	this.x = x;
    }
    
    public double getX()
    {
    	return this.x;
    }
    
    public void setY(long y)
    {
    	this.y = y;
    }
    
    public long getY()
    {
    	return this.y;
    }
}
